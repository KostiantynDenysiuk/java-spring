package com.luv2code.springcoredemo.rest;

import com.luv2code.springcoredemo.common.Coach;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.beans.factory.annotation.Qualifier;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RestController;

@RestController
public class DemoController {

    //define a private field for the dependency
    private Coach myCoach;

    //define the constructor for the dependency injection
    //Autowired annotation tells Spring to inject a dependency
    //If you have one constructor then @Autowired on constructor is optional
    @Autowired
    public DemoController(@Qualifier("cricketCoach") Coach theCoach) { //we use @primary annotation but @Qualifier overrides it
        myCoach = theCoach;
    }

    @GetMapping("/dailyworkout")
    public String getDailyWorkout() {
        return myCoach.getDailyWorkout();
    }
}
